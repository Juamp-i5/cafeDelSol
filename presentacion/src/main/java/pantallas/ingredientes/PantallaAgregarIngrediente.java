        String nombre = txtNombre.getText().trim();
        String unidadMedidaStr = (String) comboBoxUnidadMedida.getSelectedItem();
        ProveedorViejoDTO proveedorSeleccionado = (ProveedorViejoDTO) comboBoxProveedor.getSelectedItem();
