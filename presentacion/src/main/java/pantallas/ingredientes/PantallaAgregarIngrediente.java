        if (!nombre.isEmpty() && !cantidadDisponibleStr.isEmpty() && !unidadMedidaStr.isEmpty()) {
            btnGuardarIngrediente.setEnabled(true);
        } else {
            btnGuardarIngrediente.setEnabled(false);
        }

